
public class EjercicioVariables {

	public static void main(String[] args) {
		
		//Ejercicio de variables.
		//Empezamos con la variables String
		
		String texto = "Esto es un texto";
		String textoSegundo = "Esto es un segundo texto";
		String textoTercero = "Esto es un tercer texto";		
		System.out.println(texto);
		System.out.println(textoSegundo);
		System.out.println(textoTercero);
		
		texto = "Cambio de texto";
		textoSegundo = "Cambio de segundo texto";
		textoTercero = "Cambio de tercero texto";		
		System.out.println(texto);
		System.out.println(textoSegundo);
		System.out.println(textoTercero);
		
		//Variable tipo char
		
		char primero = 'a';
		char segundo = 'b';
		char tercero = 'c';
		System.out.println(primero);
		System.out.println(segundo);
		System.out.println(tercero);
		
		primero = 'd';
		segundo = 'e';
		tercero = 'f';
		System.out.println(primero);
		System.out.println(segundo);
		System.out.println(tercero);
		
		//Variable tipo boolean
		
		boolean gripe = true ;
		boolean cancer = false ;
		boolean toxoplasmosis = true ;
		System.out.println(gripe);
		System.out.println(cancer);
		System.out.println(cancer);
		
		gripe = false;
		cancer = true;
		toxoplasmosis = false;
		System.out.println(gripe);
		System.out.println(cancer);
		System.out.println(cancer);
		
		//Variable tipo double
		
		double numeroDecimal = 12.34;
		double numeroDecimalDos = 15.34;
		double numeroDecimalTres = 17.34;
		System.out.println(numeroDecimal);
		System.out.println(numeroDecimalDos);
		System.out.println(numeroDecimalTres);
		
		numeroDecimal = 34.23;
		numeroDecimalDos = 89.90;
		numeroDecimalTres = 56.34;
		System.out.println(numeroDecimal);
		System.out.println(numeroDecimalDos);
		System.out.println(numeroDecimalTres);
		
		//Variables tipo long
		long numeroLargo1 = 20303L;
		long numeroLargo2 = 25303L;
		long numeroLargo3 = 203703L;
		System.out.println(numeroLargo1);
		System.out.println(numeroLargo2);
		System.out.println(numeroLargo3);
		
		numeroLargo1 = 255500L;
		numeroLargo2 = 12121212L;
		numeroLargo3 = 1231231231L;
		System.out.println(numeroLargo1);
		System.out.println(numeroLargo2);
		System.out.println(numeroLargo3);
		
		//Variables tipo int
		int numeroEntero1 = 120;
		int numeroEntero2 = 100;
		int numeroEntero3 = 110;
		System.out.println(numeroEntero1);
		System.out.println(numeroEntero2);
		System.out.println(numeroEntero3);
		
		numeroEntero1 = 23;
		numeroEntero2 = 25;
		numeroEntero3 = 2;
		System.out.println(numeroEntero1);
		System.out.println(numeroEntero2);
		System.out.println(numeroEntero3);
		
		//Variables tipo float
		float myFloatNum = 5.99F;
		float myFloatNum2 = 5.79F;
		float myFloatNum3 = 5.89F;
		System.out.println(myFloatNum);
		System.out.println(myFloatNum2);
		System.out.println(myFloatNum3);
		
		myFloatNum = 4.99F;
		myFloatNum2 = 3.80F;
		myFloatNum3 = 2.80F;
		System.out.println(myFloatNum);
		System.out.println(myFloatNum2);
		System.out.println(myFloatNum3);
		
		//Variables tipo Byte
		byte myByte1 = 120;
		byte myByte2 = 10;
		byte myByte3 = 20;
		System.out.println(myByte1);
		System.out.println(myByte2);
		System.out.println(myByte3);
		
		myByte1 = 10;
		myByte2 = 25;
		myByte3 = 28;
		System.out.println(myByte1);
		System.out.println(myByte2);
		System.out.println(myByte3);
		
				

	}

}
