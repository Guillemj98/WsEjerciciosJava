
public class _02_PerimetroArea {
	public static void main(String[] args) {
		//Vamos a calcular el área y el perimetro de un rectángulo
		//Para ello declaramos las variables
		
			
		int longitud = 10;
		int ancho = 5;
		int area = longitud * ancho;
		int perimetro = 2 * (longitud + ancho);
		
		System.out.println("El área del rectángulo es : " + area);
		System.out.println("El perimetro del rectángulo es: " + perimetro );
		
		longitud = 7;
		ancho = 3;
		area = longitud * ancho;
		perimetro = 2 * (longitud + ancho);
		
		System.out.println("El área del rectángulo es : " + area);
		System.out.println("El perimetro del rectángulo es: " + perimetro );
		
		
		
	}

}
